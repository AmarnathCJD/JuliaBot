func init() { QueueHandlerRegistration(registerAsciiArtHandlers) }
func registerAsciiArtHandlers() {
	c := Client
	c.On("cmd:ascii", AsciiHandler)
	c.On("cmd:asciilogo", AsciiLogoHandler)
}
